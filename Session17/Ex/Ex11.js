console.log(Math.abs(-5.5)); // Kết quả: 5.5 - Hàm Math.abs() trả về giá trị tuyệt đối của số
console.log(Math.abs(10));  // Kết quả: 10 - Giá trị tuyệt đối của 10 vẫn là 10

// Sử dụng hàm Math.ceil()
console.log(Math.ceil(4.4)); // Kết quả: 5 - Làm tròn lên số nguyên gần nhất
console.log(Math.ceil(5.8)); // Kết quả: 6 - Làm tròn lên số nguyên gần nhất

// Sử dụng hàm Math.floor()
console.log(Math.floor(5.7)); // Kết quả: 5 - Làm tròn xuống số nguyên gần nhất
console.log(Math.floor(6.1)); // Kết quả: 6 - Làm tròn xuống số nguyên gần nhất

// Sử dụng hàm Math.round()
console.log(Math.round(10.1)); // Kết quả: 10 - Làm tròn đến số nguyên gần nhất, 10.1 làm tròn xuống
console.log(Math.round(20.6)); // Kết quả: 21 - Làm tròn đến số nguyên gần nhất, 20.6 làm tròn lên
